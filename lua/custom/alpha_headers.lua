module.get_random = function(current)
  local keyset = {}
  local len = 0

  for k in pairs(module.headers) do
    if module.headers[k].header ~= current then
      table.insert(keyset, k)
      len = len + 1
    end
  end
  math.randomseed(os.time())
  return module.headers[keyset[math.random(len)]]
end

module.setHeader = function(header)
  return module.headers[header]
end

module.getHeadersNames = function()
  local keyset = {}
  local n = 0

  for k, _ in pairs(module.headers) do
    n = n + 1
    keyset[n] = k
  end

  return keyset
end

module.autocomplete = function (_, _, _)
  return module.getHeadersNames()
end

return module
